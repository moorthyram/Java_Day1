public class AlphabetVowelConstant {
    public static void main(String[] args)
    {
        char vowel = 'a';
        if(vowel == 'a' || vowel == 'e' || vowel == 'i' || vowel == 'o' || vowel == 'u') {
            System.out.println(vowel + " is vowel");
        }
        else
        {
            System.out.println(vowel + " is consonant");
        }
    }
}
